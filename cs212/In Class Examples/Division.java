class Division{

    public static void main(String[] args){

        int a = 5, b = 3;
        int c = a / b;
        int d = a % b;
        double e = a / b;
        double f = a * 1.0 / b;
        System.out.println("C is " + c);
        System.out.println("D is " + d);
        System.out.println("E is " + e);
        System.out.println("F is " + f);
        
    }
}